<h1>How do I use css flexbox</h1>
<p>Flexbox is a layout model that allows elements to align and distribute space within a container. Using flexible widths and heights, elements can be aligned to fill a space or distribute space between elements, which makes it a great tool to use for responsive design systems.
<br\></p>
<p>	I have found a video that teaches us how to set up a flexbox. I can use flexbox for main things scaling, vertically and horizontally aligning, and re-ordering elements within a container.<br\>
</p>
